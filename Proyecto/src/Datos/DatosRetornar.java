package Datos;

public class DatosRetornar {
    
    public static String nombre;
    public static  int raza;
    public static int tamaño;
    public static String peso;
    public static int Actividad;
    public static int Edad;

    /**
     * @return the nombre
     */
    public static String getNombre() {
        return nombre;
    }

    /**
     * @param aNombre the nombre to set
     */
    public static void setNombre(String aNombre) {
        nombre = aNombre;
    }

    /**
     * @return the raza
     */
    public static int getRaza() {
        return raza;
    }

    /**
     * @param aRaza the raza to set
     */
    public static void setRaza(int aRaza) {
        raza = aRaza;
    }

    /**
     * @return the tamaño
     */
    public static int getTamaño() {
        return tamaño;
    }

    /**
     * @param aTamaño the tamaño to set
     */
    public static void setTamaño(int aTamaño) {
        tamaño = aTamaño;
    }

    /**
     * @return the peso
     */
    public static String getPeso() {
        return peso;
    }

    /**
     * @param aPeso the peso to set
     */
    public static void setPeso(String aPeso) {
        peso = aPeso;
    }

    /**
     * @return the Actividad
     */
    public static int getActividad() {
        return Actividad;
    }

    /**
     * @param aActividad the Actividad to set
     */
    public static void setActividad(int aActividad) {
        Actividad = aActividad;
    }

    /**
     * @return the Edad
     */
    public static int getEdad() {
        return Edad;
    }

    /**
     * @param aEdad the Edad to set
     */
    public static void setEdad(int aEdad) {
        Edad = aEdad;
    }
    
    
}
